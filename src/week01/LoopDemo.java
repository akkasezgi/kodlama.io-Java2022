package week01;

public class LoopDemo {

	public static void main(String[] args) {
		
		for (int i = 2; i < 10; i+=2) {
			System.out.println(i);
		}
		System.out.println("For dongusu bitti");

		int i=1;
		
		while(i<10) {
			System.out.println(i);
			i+=2;
			
		}
		
		System.out.println("While dongusu bitti");
	}

}
